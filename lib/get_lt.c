#include "my.h"

int get_lt(char str)
{
    if (str == 'A')
        return 1;
    if (str == 'B')
        return 2;
    if (str == 'C')
        return 3;
    if (str == 'D')
        return 4;
    if (str == 'E')
        return 5;
    if (str == 'F')
        return 6;
    if (str == 'G')
        return 7;
    if (str == 'H')
        return 8;
    return 0;
}
